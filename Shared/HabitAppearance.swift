import SwiftUI

enum HabitAppearance: String, CaseIterable, Codable, Hashable, Sendable, Identifiable {
    case system
    case light
    case dark

    var id: String { rawValue }

    var colorScheme: ColorScheme? {
        switch self {
        case .system: nil
        case .light: .light
        case .dark: .dark
        }
    }
}
